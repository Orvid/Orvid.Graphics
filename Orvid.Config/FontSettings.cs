using System;

namespace Orvid.Config
{
	/// <summary>
	/// This class contains the settings for
	/// font rendering.
	/// </summary>
	public static class FontSettings
	{
		/// <summary>
		/// This setting controls the 
		/// Dots Per Inch (DPI) that 
		/// fonts are rendered at.
		/// </summary>
		public static int DotsPerInch = 96;
		/// <summary>
		/// This setting is the vertical
		/// Dots Per Inch (DPI) of the screen.
		/// </summary>
		public static int VerticalDPI = 96;
		/// <summary>
		/// This setting is the horizontal
		/// Dots Per Inch (DPI) of the screen.
		/// </summary>
		public static int HorizontalDPI = 96;
	}
}
